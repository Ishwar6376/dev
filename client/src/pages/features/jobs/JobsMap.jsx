export default function JobsMap({
  jobs,
  selectedJob,
  onSelect,
  userLocation,
}) {
  const { isLoaded } = useJsApiLoader({
    googleMapsApiKey: import.meta.env.VITE_GOOGLE_MAPS_API_KEY,
  });


  if (!isLoaded) {
    return (
      <div className="h-full flex items-center justify-center bg-zinc-950">
        <Loader2 className="animate-spin text-zinc-500" />
      </div>
    );
  }

  return (
    <GoogleMap
      mapContainerStyle={containerStyle}
      center={userLocation}
      zoom={13}
      options={mapOptions}
    >
      {/* JOB MARKERS */}
      {jobs.map((job) => (
        <Marker
          key={job.id}
          position={job.location}
          onClick={() => onSelect(job)}
          icon={{
            url: selectedJob?.id === job.id
                ? "http://maps.google.com/mapfiles/ms/icons/green-dot.png" // Highlighted color (usually red/blue default)
                : "http://maps.google.com/mapfiles/ms/icons/blue-dot.png", // Default color
          }}
        />
      ))}
    </GoogleMap>
  );
}